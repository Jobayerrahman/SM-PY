# Integer (int) in Python represents whole numbers.
# These can be positive, negative, or even very large numbers (Python has no limit for integer size).

x = 1                          # Small positive integer
y = 2345252351542314551        # Very large integer
z = -3255522                   # Negative integer

print(type(x))  # Output: <class 'int'>
print(type(y))  # Output: <class 'int'>
print(type(z))  # Output: <class 'int'>
