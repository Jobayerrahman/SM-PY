#Copy list with copy() method:
print("Copy list with copy() method : ")
stringList01 = ["apple", "banana", "cherry"]
stringList02 = stringList01.copy()
print(stringList02)

#Copy list with list() method:
print("Copy list with list() method : ")
stringList01 = ["apple", "banana", "cherry"]
stringList02 = list(stringList01)
print(stringList02)


