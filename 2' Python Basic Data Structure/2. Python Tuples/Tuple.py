#Tuples
fruitsTuple = ('apple','banana','cherry')
print(fruitsTuple)

#Tuples allow duplicate values
duplicateFruitsTuple = ('apple','banana','cherry','banana','cherry')
print(duplicateFruitsTuple)

#Tuples length property
print(len(fruitsTuple))
print(len(duplicateFruitsTuple))

#Type of Tuples
print(type(fruitsTuple))
print(type(duplicateFruitsTuple))

#Tuple Items - Data Types
tuple1 = ("apple", "banana", "cherry")
tuple2 = (1, 5, 7, 9, 3)
tuple3 = (True, False, False)

#A tuple can contain different data types
differentTypesTuples = ('apple','banana',1,False,4,6,True)
print(differentTypesTuples)

#The tuple() Constructor
constructorTuple = tuple(("apple", "banana", "cherry"))
print(constructorTuple)